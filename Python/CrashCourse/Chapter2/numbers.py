# Integers

print(2 + 3) # Addition
print(2 - 3) # Subtraction
print(2 * 3) # Multiplication
print(3 / 2) # Division Python3 returns float but Python2 returns a stripped integer
print(3 ** 2) # Exponent

# Float

print(0.1 + 0.2) # Addition
print(0.1 - 0.2) # Subtraction
print(0.1 * 0.2) # Multiplication
print(0.1 / 0.2) # Division Python3 returns float but Python2 returns a stripped integer
print(0.1 ** 0.2) # Exponent
