cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort() # Sort alphabetically
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True) # Sort alphabetically but backwards
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
print(sorted(cars)) # Print temporarily sorted list
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.reverse() # Reverse the order by position
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
print(len(cars))
