print("Python\n")

# Tabs
print("\tPython\n")

# New Lines
print("Languages:\nPython\nC\nJavascript\n")

# New Lines and Tabs
print("Languages:\n\tPython\n\tC\n\tJavascript\n")

# Strip Whitespace
favorite_language = " python "
print(favorite_language.rstrip()) # strip from the right
print(favorite_language.lstrip()) # strip from the left