# Integers

# Addition
a = (2 + 3)
print(a)

# Subtraction
b = (3 - 2)
print(b)

# Multiplication
c = (2 * 3)
print(c)

# Division
d = (3 / 2)
print(d)

# Exponentiation
e = (3 ** 3)
print(e)

# Order of Operations
f = 2 + 3 * 4
print(f) # Output should be 14

g = (2 + 3) * 4
print(g) # Output should be 20

# Floats
h = 0.1 + 0.2
print(h)

# Python defaults all operation expression to a float.

i = 1 + 2.0
print(i) # Should be a float

j = 4 / 2
print(j) # Should be a float

# Underscores
universe_age = 14_000_000_000
print(universe_age)

# Multiple Assignments
x, y, z = 5, 10, 15
print(x, y, z)

